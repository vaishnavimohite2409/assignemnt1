# Basic Calculations
# Calculator Program in Python by using input() and format() functions

#Promting input from the user

a1 = float(input("Enter the First Number: "))
a2 = float(input("Enter the Second Number: "))

#addition

print("{} + {} = ".format(a1, a2))
print(a1 + a2)

#subtraction

print("{} - {} = ".format(a1, a2))
print(a1 - a2)

#multiplication

print("{} * {} = ".format(a1, a2))
print(a1 * a2)

#division

print("{} / {} = ".format(a1, a2))
print(a1 / a2)